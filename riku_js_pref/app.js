function sumple(sports){
    if (sports === "野球"){
        return("イチロー");
    }
    else if (sports === "サッカー"){
        return("キングカズ");
    }
    else if (sports === "バスケットボール"){
        return("八村塁");
    }
}

console.log(sumple("野球"));
console.log(sumple("サッカー"));
console.log(sumple("バスケットボール"));